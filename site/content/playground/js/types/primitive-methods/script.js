
var name = "Lexis";
var loverCaseName = name.toLowerCase();
var firstLetter = name.charAt(0);
var middleOfName = name.substring(2, 5);

console.log(name);            // Lex
console.log(loverCaseName);   // lex
console.log(firstLetter);     // L
console.log(middleOfName);    // xis 

